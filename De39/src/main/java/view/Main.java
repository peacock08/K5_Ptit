    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int n= listPB.size();
        if (n >0)
            PhongBan.setSma(listPB.get(n-1).getMa()+1);
        txtmaPhong.setText(PhongBan.getSma()+"");
        String ten, mota;
        double heso;
        try {
            ten = txttenPhong.getText();
            mota = txtmota.getText();
            heso=Double.parseDouble(txtheso.getText());
            
            PhongBan pb = new PhongBan(ten, mota, heso);
            tmPB.addRow(pb.toObjects());
            listPB.add(pb);
            txtmota.setText("");
            txttenPhong.setText("");
            txtheso.setText("");
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "khong de trong");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "nhap so!");
            return;
        } catch (HeSoException ex) {
             JOptionPane.showMessageDialog(this, "he so nam trong khoang tu 1 den 20");
            return;
        }
    }//GEN-LAST:event_jButton5ActionPerformed

