    private void loadNV() {
        File f =  new File(fNV);
        if (f.exists())
            listNV = IOFile.doc(fNV);
        else
            listNV = new ArrayList<>();
    }
    private void loadPB() {
        File f =  new File(fPB);
        if (f.exists())
            listPB = IOFile.doc(fPB);
        else
            listPB = new ArrayList<>();
    }
    
    private void showNV(List<NhanVien> list) {
        tmNV.setRowCount(0);
        for (NhanVien i : list)
            tmNV.addRow(i.toObjects());
    }
    private void showPB(List<PhongBan> list) {
        tmPB.setRowCount(0);
        for (PhongBan i : list)
            tmPB.addRow(i.toObjects());
    }

