    private void ntNhapNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ntNhapNVActionPerformed
        // TODO add your handling code here:
        int n = listNV.size();
        if (n > 0) 
            NhanVien.setSma(listNV.get(n-1).getMa()+1);
        txtMaNV.setText(NhanVien.getSma()+"");
        String ten, diachi, sodt;
        try {
            ten = txtTenNV.getText();
            diachi = txtDiaChi.getText();
            sodt = txtDienthoai.getText();
            NhanVien nv = new NhanVien(ten, diachi, sodt);
            tmNV.addRow(nv.toObjects());
            listNV.add(nv);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
        } catch (DienThoaiException ex) {
            JOptionPane.showMessageDialog(this, "Vui long dien dung dinh dang so dien thoai");
            return;
        }
       
    }//GEN-LAST:event_ntNhapNVActionPerformed

    private void btNhapMatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNhapMatHangActionPerformed
        // TODO add your handling code here:
        int n = listMH.size();
        if ( n > 0)
            MatHang.setSma(listMH.get(n-1).getMa()+1);
        txtMaHang.setText(MatHang.getSma()+"");
        String ten, loai;
        double giaban;
        int soluong;
        try {
            ten = txtTenHang.getText();
            loai = jComboBox1.getSelectedItem().toString();
            giaban = Double.parseDouble(txtGiaBan.getText());
            soluong = Integer.parseInt(txtSoLuong.getText());
            MatHang mh = new MatHang(ten, loai, giaban, soluong);
            tmMh.addRow(mh.toObjects());
            listMH.add(mh);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui long nhap so");
            return;
        }
    
    }//GEN-LAST:event_btNhapMatHangActionPerformed

