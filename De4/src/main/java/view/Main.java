    private void loadMH() {
        File f = new File(fMH);
        if (f.exists())
            listMH = IOFile.doc(fMH);
        else
            listMH = new ArrayList<>();
    }
    
    private void showMH(List<MatHang> list) {
        tmMh.setRowCount(0);
        for (MatHang i : list) 
            tmMh.addRow(i.toObjects());
    }
    
