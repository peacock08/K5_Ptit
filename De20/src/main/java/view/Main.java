    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n = listKH.size();
        if (n >0)
            KhachHang.setSma(listKH.get(n-1).getMa()+1);
        txtmaKH.setText(KhachHang.getSma()+"");
        String ten, diachi, dienthoai, loai;
        try {
            ten = txttenKH.getText();
            diachi = txtdiachi.getText();
            dienthoai = txtsdt.getText();
            loai = jComboBox1.getSelectedItem().toString();
            KhachHang kh = new KhachHang(ten, diachi, dienthoai, loai);
            tmKH.addRow(kh.toObjects());
            listKH.add(kh);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
        } catch (DienThoaiException ex) {
             JOptionPane.showMessageDialog(this, "Vui long dien dun dinh dang so dien thoai");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

