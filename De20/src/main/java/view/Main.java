    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        if (r >=0 && r <jTable1.getRowCount()) {
            String ten = txttenKH.getText();
            String diachi = txtdiachi.getText();
            String sdt = txtsdt.getText();
            String loai = jComboBox1.getSelectedItem().toString();
            tmKH.setValueAt(ten, r, 1);
            tmKH.setValueAt(diachi, r, 2);
            tmKH.setValueAt(sdt, r, 3);
            tmKH.setValueAt(loai, r, 4);
            int ma = Integer.parseInt(txtmaKH.getText());
            KhachHang kh = new KhachHang(ma, ten, diachi, diachi, loai);
            listKH.set(r, kh);
                     
        }
        else {
            JOptionPane.showMessageDialog(this, "Chon khach hang de sua");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

