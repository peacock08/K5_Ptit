    private void loadKH() {
        File f = new File(fKH);
        if (f.exists())
            listKH = IOFile.doc(fKH);
        else
            listKH = new ArrayList<>();           
    }
     private void loadPhong() {
        File f = new File(fP);
        if (f.exists())
            listP = IOFile.doc(fP);
        else
            listP = new ArrayList<>();           
    }
    
    private void showKH(List<KhachHang> list) {
        tmKH.setRowCount(0);
        for (KhachHang i : list)
            tmKH.addRow(i.toObjects());
    }
    private void showPhong(List<Phong> list) {
        tmP.setRowCount(0);
        for (Phong i : list)
            tmP.addRow(i.toObjects());
    }
