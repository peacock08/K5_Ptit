     private void loadPH() {
         File f = new File(fPH);
         if (f.exists())
             listPH = IOFile.doc(fPH);
         else
             listPH = new ArrayList<>();
     }
     
     private void showPH(List<PhongHoc> list) {
         tmPH.setRowCount(0);
         for (PhongHoc i :list) 
             tmPH.addRow(i.toObjects());
     }
     
     private void loadTB() {
         File f = new File(fTB);
         if (f.exists())
             listTB = IOFile.doc(fTB);
         else
             listTB = new ArrayList<>();
     }
     
     private void showTB(List<ThietBi> list) {
         tmTB.setRowCount(0);
         for (ThietBi i : list) 
             tmTB.addRow(i.toObjects());
     }
