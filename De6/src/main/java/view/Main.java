    private void btNhapPHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNhapPHActionPerformed
        // TODO add your handling code here:
        int n = listPH.size();
        if (n >0)
            PhongHoc.setSma(listPH.get(n-1).getMa()+1);
        txtmaPhong.setText(PhongHoc.getSma()+"");
        String ten;
        int socho;
        try {
            ten = txttenPhong.getText();
            socho = Integer.parseInt(txtsoGhe.getText());
            PhongHoc ph = new PhongHoc(ten, socho);
            tmPH.addRow(ph.toObjects());
            listPH.add(ph);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui long dien so");
            return;
        }
    }//GEN-LAST:event_btNhapPHActionPerformed

