    private void btNhapTBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNhapTBActionPerformed
        // TODO add your handling code here:
        int n= listTB.size();
        if (n>0)
            ThietBi.setSma(listTB.get(n-1).getMa()+1);
        txtmaTB.setText(ThietBi.getSma()+"");
        String ten, xuatxu;
        int namsx;
        try {
            ten = txttenTB.getText();
            xuatxu = jComboBox1.getSelectedItem().toString();
            namsx = Integer.parseInt(txtnamSX.getText());
            ThietBi tb = new ThietBi(ten, xuatxu, namsx);
            tmTB.addRow(tb.toObjects());
            listTB.add(tb);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
        } catch (NamException e) {
            JOptionPane.showMessageDialog(this, "Nam khong hop le vui long nhap lai");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui long dien so");
            return;
        }
    }//GEN-LAST:event_btNhapTBActionPerformed

