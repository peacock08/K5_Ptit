    private void loadMT() {
        File f = new File(fMT);
        if (f.exists())
            listMT = IOFile.doc(fMT);
        else
            listMT = new ArrayList<>();
    }
    private void loadP() {
        File f = new File(fPTH);
        if (f.exists())
            listPTH = IOFile.doc(fPTH);
        else
            listPTH = new ArrayList<>();
    }
    
    private void showMT(List<MayTinh> list) {
        tmMT.setRowCount(0);
        for (MayTinh i : list)
            tmMT.addRow(i.toObjects());
    }
    private void showP(List<PhongTH> list) {
        tmPTH.setRowCount(0);
        for (PhongTH i : list)
            tmPTH.addRow(i.toObjects());
    }

