    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        if (r >=0 && r <jTable2.getRowCount()){
            String ten = txttenPhong.getText();
            int somay = Integer.parseInt(txtsoMay.getText());
            tmPTH.setValueAt(ten, r, 1);
            tmPTH.setValueAt(somay, r, 2);
            int ma = Integer.parseInt(txtmaPhong.getText());
            PhongTH p = new PhongTH(ma, ten, somay);
            listPTH.set(r, p);
        }
        else {
            JOptionPane.showMessageDialog(this, "Chon phong de sua");
            return;
        }
    }//GEN-LAST:event_jButton8ActionPerformed

