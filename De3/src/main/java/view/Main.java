    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        if (r >=0 && r <jTable1.getRowCount()){
            String tenMH = txttenHang.getText();
            String nhom = jComboBox1.getSelectedItem().toString();
            double giaban = Double.parseDouble(txtgiaBan.getText());
            tmMH.setValueAt(tenMH, r, 1);
            tmMH.setValueAt(nhom, r, 2);
            tmMH.setValueAt(giaban, r, 3);
            int ma =Integer.parseInt(txtmaHang.getText());
            MatHang mh = new MatHang(ma, tenMH, nhom, giaban);
            listMH.set(r, mh);
        }
        else{
            JOptionPane.showMessageDialog(this, "Chon mat han de sua");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

