    private void btThemMatHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemMatHangActionPerformed
        // TODO add your handling code here:
        int n = listMH.size();
        if (n>0)
            MatHang.setSma(listMH.get(n-1).getMa()+1);
        txtmaHang.setText(MatHang.getSma()+"");
        String ten, nhom;
        double gia;
        try {
            ten = txttenHang.getText();
            nhom = jComboBox1.getSelectedItem().toString();
            gia = Double.parseDouble(txtgiaBan.getText());
            MatHang mh = new MatHang(ten, nhom, gia);
            tmMH.addRow(mh.toObjects());
            listMH.add(mh);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui long nnhap so");
            return;
        }

    
    }//GEN-LAST:event_btThemMatHangActionPerformed

    private void btThemKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemKhachHangActionPerformed
        // TODO add your handling code here:
        int n = listKH.size();
        if (n > 0)
            KhachHang.setSma(listKH.get(n-1).getMa()+1);
        txtmaKH.setText(KhachHang.getSma()+"");
        String ten, diachi, sodt;
        try {
            ten = txttenKH.getText();
            diachi = txtDiaCHi.getText();
            sodt = txtsdt.getText();
            KhachHang kh = new KhachHang(ten, diachi, sodt);
            tmKH.addRow(kh.toObjects());
            listKH.add(kh);
        } catch (DienThoaiException e) {
            JOptionPane.showMessageDialog(this, "Vui long dien dung dinh dang so dien thoai");
            return;
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "Khong de trong");
            return;
        }
    
    }//GEN-LAST:event_btThemKhachHangActionPerformed

