    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        if (r >=0 && r <jTable1.getRowCount()) {
            String name = txtName.getText();
            String birth = txtBirth.getText();
            String address = txtAddress.getText();
            String classes = txtClass.getText();
            tmSV.setValueAt(name, r, 1);
            tmSV.setValueAt(birth, r, 2);
            tmSV.setValueAt(address, r, 3);
            tmSV.setValueAt(classes, r, 4);
            int MSV = Integer.parseInt(txtMSV.getText());
            SinhVien sv =  new SinhVien(MSV, name, birth, address, classes);
            listsv.set(r, sv);
        }
        else {
            JOptionPane.showMessageDialog(this, "chon sinh vien de sua");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

