    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n = listsv.size();
        if ( n > 0) 
            SinhVien.setSma(listsv.get(n-1).getMa()+1);
        txtMSV.setText(SinhVien.getSma()+"");
        String  hoten, ngaysinh, diachi, lop;
        try {
            hoten = txtName.getText();
            ngaysinh = txtBirth.getText();
            diachi = txtAddress.getText();
            lop = txtClass.getText();
            SinhVien sv = new SinhVien(hoten, ngaysinh, diachi, lop);
            tmSV.addRow(sv.toObjects());
            listsv.add(sv);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "khong de trong");
            return;
        } catch (MaException ex) {
           JOptionPane.showMessageDialog(this, "dien dung dinh dang ma sinh vien");
            return;
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

