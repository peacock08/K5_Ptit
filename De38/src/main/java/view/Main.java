    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow();
        if (r >=0 && r <jTable2.getRowCount()) {
            String ten = txttenKH.getText();
            String kieu = jComboBox2.getSelectedItem().toString();
            tmKH.setValueAt(ten, r, 1);
            tmKH.setValueAt(kieu, r, 2);
            int ma = Integer.parseInt(txtmaKH.getText());
            KhachHang kh =  new KhachHang(ma, ten, kieu);
            listKH.set(r, kh);
            txttenKH.setText("");
        }
        else {
            JOptionPane.showMessageDialog(this, "chon ktour de sua");
            return;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String key = txttimKH.getText().toLowerCase().trim();
        List<KhachHang> list = new ArrayList<>();
        for (KhachHang i : listKH) {
            if (i.getTen().toLowerCase().contains(key)
                    ||i.getKieu().toLowerCase().contains(key))
                list.add(i);
        }
        showKH(list);
    }//GEN-LAST:event_jButton10ActionPerformed

