    private void loadT() {
        File f = new File(fT);
        if (f.exists())
            listT = IOFile.doc(fT);
        else
            listT = new ArrayList<>();
    }
    private void loadKH() {
        File f = new File(fKH);
        if (f.exists())
            listKH = IOFile.doc(fKH);
        else
            listKH = new ArrayList<>();
    }
    
    private void showT(List<Tour> list) {
        tmT.setRowCount(0);
        for (Tour i :list)
            tmT.addRow(i.toObjects());
    }
    private void showKH(List<KhachHang> list) {
        tmKH.setRowCount(0);
        for (KhachHang i :list)
            tmKH.addRow(i.toObjects());
    }
