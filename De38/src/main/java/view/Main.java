    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n = listT.size();
        if (n > 0)
            Tour.setSma(listT.get(n-1).getMa()+1);
        txtmaTour.setText(Tour.getSma()+"");
        String kieu, thongtin;
        double dongia;
        try {
            kieu = jComboBox2.getSelectedItem().toString();
            thongtin = txtthongtin.getText();
            dongia = Double.parseDouble(txtDongia.getText());
            Tour t = new Tour(kieu, thongtin, dongia);
            tmT.addRow(t.toObjects());
            listT.add(t);
            txtthongtin.setText("");
            txtDongia.setText("");
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "vui long khong de trong");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "nhap so!");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

