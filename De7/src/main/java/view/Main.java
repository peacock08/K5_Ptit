    private void loadDA() {
        File f = new File(fDA);
        if (f.exists())
            listDA = IOFile.doc(fDA);
        else
            listDA  = new ArrayList<>();          
    }
    private void showDA(List<DuAn> list) {
        tmDA.setRowCount(0);
        for (DuAn i : list) 
            tmDA.addRow(i.toObjects());
    }
    
