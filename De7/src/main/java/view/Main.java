    private void btThemDAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemDAActionPerformed
        // TODO add your handling code here:
        int n = listDA.size();
        if (n >0)
            DuAn.setSma(listDA.get(n-1).getMa()+1);
        txtMaDA.setText(DuAn.getSma()+"");
        String ten, kieu;
        double kinhphi;
        try {
            ten = txtTenDA.getText();
            kieu = jComboBox1.getSelectedItem().toString();
            kinhphi = Double.parseDouble(txtKinhPhi.getText());
            DuAn da = new DuAn(ten, kieu, kinhphi);
            tmDA.addRow(da.toObjects());
            listDA.add(da);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "khong de trong");
            return;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "nhap so");
            return;
        }
    }//GEN-LAST:event_btThemDAActionPerformed

