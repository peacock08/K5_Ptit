    private void btSuaNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaNVActionPerformed
        // TODO add your handling code here:
        int r= jTable1.getSelectedRow();
        if (r >=0 && r <jTable1.getRowCount()){
            String hoten = txttenNV.getText();
            String diachi = txtDiaChi.getText();
            String chuyenmon = txtChuyenMon.getText();
            tmNV.setValueAt(hoten, r, 1);
            tmNV.setValueAt(diachi, r, 2);
            tmNV.setValueAt(chuyenmon, r, 3);
            int ma = Integer.parseInt(txtmaNV.getText());
            NhanVien nv = new NhanVien(ma, hoten, diachi, chuyenmon);
            listNV.set(r, nv);
            
        } else {
            JOptionPane.showMessageDialog(this, "Chon dich vu de sua");
            return;
        }

    }//GEN-LAST:event_btSuaNVActionPerformed

