    private void btThemNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemNVActionPerformed
        // TODO add your handling code here:
        int  n= listNV.size();
        if (n>0)
            NhanVien.setSma(listNV.get(n-1).getMa()+1);
        txtmaNV.setText(NhanVien.getSma()+"");
        String hoten, diachi, chuyenmon;
        try {
            hoten = txttenNV.getText();
            diachi = txtDiaChi.getText();
            chuyenmon = txtChuyenMon.getText();
            NhanVien nv = new NhanVien(hoten, diachi, chuyenmon);
            tmNV.addRow(nv.toObjects());
            listNV.add(nv);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "khong de trong");
            return;
        }
                
    }//GEN-LAST:event_btThemNVActionPerformed

